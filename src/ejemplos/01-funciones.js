const existError = (email, password) => {
  let isInvalid = true;
  if (email && password) {
    isInvalid = false;
  }
  return isInvalid;
};

const isAuthenticated = (email, password) => {
  const userAdmin = {
    email: "rollingCode@gmail.com",
    password: "123123",
  };
  if (email === userAdmin.email && password === userAdmin.password) {
    return true;
  }
  return false;
};

const login = (email, password) => {
  if (!existError(email, password)) {
    if (isAuthenticated(email, password)) {
      return "Bienvenido a Rolling Code";
    }
    return "El email o contraseña son incorrectos";
  }
  return "El email o contraseña no pueden estar vacíos";
};

